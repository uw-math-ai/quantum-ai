def expand_stim_line(line):
    '''Expand a single Stim line to individual gate operations'''
    parts = line.strip().split()
    if not parts:
        return []
    gate = parts[0]
    targets = [int(x) for x in parts[1:]]
    
    result = []
    if gate in ['CX', 'CZ', 'SWAP']:
        for i in range(0, len(targets), 2):
            if i+1 < len(targets):
                result.append(f'{gate} {targets[i]} {targets[i+1]}')
    elif gate in ['H', 'S', 'X', 'Y', 'Z']:
        for t in targets:
            result.append(f'{gate} {t}')
    else:
        result.append(line)
    return result

def expand_circuit(raw_text):
    '''Expand entire circuit'''
    result = []
    for line in raw_text.strip().split('\n'):
        result.extend(expand_stim_line(line))
    return '\n'.join(result)

