stabilizers_list = [s.strip() for s in stabilizers_str.strip().split('\n')]
baseline_circuit = stim.Circuit(baseline_circuit_str)

def count_cx(circuit):
    cnt = 0
    for inst in circuit:
        if inst.name == "CX":
            cnt += len(inst.targets_copy()) // 2
    return cnt

def count_vol(circuit):
    cnt = 0
    for inst in circuit:
        if inst.name in ["CX", "CY", "CZ", "H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "X", "Y", "Z"]:
             cnt += len(inst.targets_copy()) // (2 if inst.name in ["CX", "CY", "CZ"] else 1)
    return cnt

print(f"Baseline CX count: {count_cx(baseline_circuit)}")
# print(f"Baseline Volume: {count_vol(baseline_circuit)}")

# Check if stabilizers are preserved by baseline
# First, create a Tableau from the circuit
sim = stim.TableauSimulator()
sim.do(baseline_circuit)

valid = True
for i, s in enumerate(stabilizers_list):
    p = stim.PauliString(s)
    if sim.peek_observable_expectation(p) != 1:
        # print(f"Stabilizer {i} NOT preserved by baseline")
        valid = False
        # break
if valid:
    print("Baseline preserves all stabilizers")
else:
    print("Baseline does NOT preserve all stabilizers (this is unexpected)")

# Attempt Synthesis
try:
    # Tableau.from_stabilizers creates a tableau T such that T|0> is stabilized by the stabilizers.
    # We need to handle the case where the stabilizers are underconstrained (50 stabs for 54 qubits).
    # allow_underconstrained=True will fill the remaining degrees of freedom arbitrarily (usually with Z or I).
    
    t = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in stabilizers_list], allow_underconstrained=True)
    
    # Synthesis
    synth_circuit = t.to_circuit(method="elimination")
    
    # Verify synthesis
    sim_synth = stim.TableauSimulator()
    sim_synth.do(synth_circuit)
    valid_synth = True
    for s in stabilizers_list:
        if sim_synth.peek_observable_expectation(stim.PauliString(s)) != 1:
            valid_synth = False
            break
            
    print(f"Synthesized CX count: {count_cx(synth_circuit)}")
    print(f"Synthesized Valid: {valid_synth}")
    
    if valid_synth and count_cx(synth_circuit) < count_cx(baseline_circuit):
        print("Synthesis found a better circuit!")
        print(synth_circuit)

except Exception as e:
    print(f"Synthesis error: {e}")
