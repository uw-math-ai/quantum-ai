    covered_indices = set()
    chosen_stabs = []
    
    while len(covered_indices) < len(faults):
        best_stab = -1
        best_cover = []
        
        for i, s in enumerate(stabilizers):
            current_cover = []
            for f_idx, fault in enumerate(faults):
                if f_idx in covered_indices: continue
                if check_anticommute(fault['final_paulis'], s):
                    current_cover.append(f_idx)
            
            # Prefer larger cover. Tie break: lower weight?
            # Or prefer stabilizers with specific properties?
            # Let's just maximize cover.
            if len(current_cover) > len(best_cover):
                best_cover = current_cover
                best_stab = i
        
        if not best_cover:
            print(f"Cannot cover remaining {len(faults) - len(covered_indices)} faults")
            break
            
        print(f"Stabilizer {best_stab} covers {len(best_cover)} faults: {best_cover}")
        chosen_stabs.append(best_stab)
        covered_indices.update(best_cover)
        
    print(f"Chosen stabilizers indices: {chosen_stabs}")
