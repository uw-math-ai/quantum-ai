stabs = [s.strip() for s in stabilizers_str.strip().split('\n')]
ps = [stim.PauliString(s) for s in stabs]
tableau = stim.Tableau.from_stabilizers(ps, allow_underconstrained=True)

# Get the graph_state circuit
raw_circuit = tableau.to_circuit(method='graph_state')

# Build new circuit: replace RX with H (since starting from |0>)
lines = []
for inst in raw_circuit:
    name = inst.name
    targets = inst.targets_copy()
    if name == 'RX':
        # RX on |0> is equivalent to H (up to global phase)
        qubits = [str(t.value) for t in targets]
        lines.append(f"H {' '.join(qubits)}")
    elif name == 'TICK':
        pass  # Skip TICK
    elif name == 'CZ':
        pairs = [(targets[i].value, targets[i+1].value) for i in range(0, len(targets), 2)]
        pair_str = ' '.join(f"{a} {b}" for a, b in pairs)
        lines.append(f"CZ {pair_str}")
    elif name in ['X', 'Y', 'Z', 'S', 'S_DAG', 'H', 'SQRT_X', 'SQRT_X_DAG']:
        qubits = [str(t.value) for t in targets]
        lines.append(f"{name} {' '.join(qubits)}")
    elif name == 'CX':
        pairs = [(targets[i].value, targets[i+1].value) for i in range(0, len(targets), 2)]
        pair_str = ' '.join(f"{a} {b}" for a, b in pairs)
        lines.append(f"CX {pair_str}")
    else:
        print(f"Unknown: {name}")

circuit_str = '\n'.join(lines)
print(circuit_str)
