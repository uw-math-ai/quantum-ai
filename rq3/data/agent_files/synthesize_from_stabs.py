# Convert to matrix form for analysis
def stab_to_binary(stab):
    """Convert stabilizer string to binary (x_vec, z_vec)"""
    n = len(stab)
    x = np.zeros(n, dtype=int)
    z = np.zeros(n, dtype=int)
    for i, c in enumerate(stab):
        if c == 'X':
            x[i] = 1
        elif c == 'Z':
            z[i] = 1
        elif c == 'Y':
            x[i] = 1
            z[i] = 1
    return x, z

# Build full binary matrix
X_mat = []
Z_mat = []
for s in stabilizers:
    x, z = stab_to_binary(s)
    X_mat.append(x)
    Z_mat.append(z)

X_mat = np.array(X_mat)
Z_mat = np.array(Z_mat)

print(f"X matrix shape: {X_mat.shape}")
print(f"Z matrix shape: {Z_mat.shape}")

# Count non-trivial entries
x_nonzero = np.sum(X_mat)
z_nonzero = np.sum(Z_mat)
print(f"Non-zero X: {x_nonzero}, Non-zero Z: {z_nonzero}")

# Analyze which qubits are involved
x_per_qubit = np.sum(X_mat, axis=0)
z_per_qubit = np.sum(Z_mat, axis=0)
print(f"Qubits with X: {np.sum(x_per_qubit > 0)}")
print(f"Qubits with Z: {np.sum(z_per_qubit > 0)}")

# Check commutativity
def commute(stab1, stab2):
    """Check if two stabilizers commute (symplectic inner product = 0)"""
    x1, z1 = stab_to_binary(stab1)
    x2, z2 = stab_to_binary(stab2)
    # Inner product: sum of (x1*z2 + z1*x2) mod 2
    return (np.sum(x1 * z2) + np.sum(z1 * x2)) % 2 == 0

# Check all pairs commute
all_commute = True
for i in range(len(stabilizers)):
    for j in range(i+1, len(stabilizers)):
        if not commute(stabilizers[i], stabilizers[j]):
            print(f"Stabilizers {i} and {j} anticommute!")
            all_commute = False
            break
    if not all_commute:
        break

if all_commute:
    print("All stabilizers commute - consistent set!")

