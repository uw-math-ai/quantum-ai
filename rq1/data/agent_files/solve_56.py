def solve():
    try:
        # Convert to Stim PauliString objects
        pauli_stabilizers = [stim.PauliString(s) for s in stabilizers]
        
        # Create a tableau from the stabilizers
        # allow_underconstrained=True lets it pick any state that satisfies the stabilizers
        tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
        
        # Convert to circuit
        circuit = tableau.to_circuit()
        
        # Output the circuit
        with open("circuit_56.stim", "w") as f:
            f.write(str(circuit))
            
        print("Circuit generated successfully.")
        
    except Exception as e:
        print(f"Error: {e}")

if __name__ == "__main__":
    solve()
