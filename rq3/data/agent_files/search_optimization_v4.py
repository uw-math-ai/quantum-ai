def count_cx(circuit):
    cnt = 0
    for inst in circuit:
        if inst.name == "CX":
            cnt += len(inst.targets_copy()) // 2
    return cnt

def count_vol(circuit):
    cnt = 0
    for inst in circuit:
        if inst.name in ["CX", "CY", "CZ", "H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "X", "Y", "Z"]:
             cnt += len(inst.targets_copy()) // (2 if inst.name in ["CX", "CY", "CZ"] else 1)
    return cnt

best_cx = 237
best_vol = 999999
best_circuit = None
best_order = None

def check_order(order, name):
    global best_cx, best_vol, best_circuit, best_order
    try:
        t = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in order], allow_underconstrained=True)
        c = t.to_circuit(method="elimination")
        cx = count_cx(c)
        vol = count_vol(c)
        if cx < best_cx or (cx == best_cx and vol < best_vol):
            print(f"Found better ({name}): CX={cx}, Vol={vol}")
            best_cx = cx
            best_vol = vol
            best_circuit = c
            best_order = list(order)
            return True
    except Exception as e:
        pass
    return False

