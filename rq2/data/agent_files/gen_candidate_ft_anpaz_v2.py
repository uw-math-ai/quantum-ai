def get_indices(stabilizer):
    indices = []
    pauli = 'I'
    for i, c in enumerate(stabilizer):
        if c in ['X', 'Z']:
            indices.append(i)
            if pauli == 'I': pauli = c
            elif pauli != c: pauli = 'Mixed'
    return indices, pauli

def create_flag_gadget(qubit_indices, stab_type, next_ancilla):
    M = next_ancilla
    F = next_ancilla + 1
    snippet = ""
    if stab_type == 'X':
        # Measure X parity. M |0>. F |0>. H F. CX q M. CX F M. M M. H F. M F.
        snippet += f"H {F}\n"
        for q in qubit_indices:
            snippet += f"CX {q} {M}\n"
        snippet += f"CX {F} {M}\n"
        snippet += f"M {M}\n"
        snippet += f"H {F}\n"
        snippet += f"M {F}\n"
    elif stab_type == 'Z':
        # Measure Z parity. M |+>. F |0>. CX M q. CX M F. M M. M F.
        snippet += f"H {M}\n"
        for q in qubit_indices:
            snippet += f"CX {M} {q}\n"
        snippet += f"CX {M} {F}\n"
        snippet += f"H {M}\n"
        snippet += f"M {M}\n"
        snippet += f"M {F}\n"
    return snippet, next_ancilla + 2

