def count_gates(circuit):
    cx_count = 0
    cz_count = 0
    volume = 0
    for instr in circuit:
        name = instr.name
        tgt = instr.targets_copy()
        if name in ['CX', 'CNOT']:
            cx_count += len(tgt) // 2
        elif name == 'CZ':
            cz_count += len(tgt) // 2
        volume += len(tgt)
    return cx_count, cz_count, volume

# Convert strings to PauliString objects
pauli_strings = [stim.PauliString(s) for s in stabilizers]

tableau = stim.Tableau.from_stabilizers(
    pauli_strings,
    allow_redundant=True,
    allow_underconstrained=True
)

print("Testing different synthesis methods...")
methods = ['graph_state', 'elimination']

for method in methods:
    try:
        circuit = tableau.to_circuit(method=method)
        cx, cz, vol = count_gates(circuit)
        print(f"\n{method}: CX={cx}, CZ={cz}, Volume={vol}")
    except Exception as e:
        print(f"\n{method}: Error - {e}")

