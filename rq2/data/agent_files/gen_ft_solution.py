stabs = get_stabilizers()
original_circuit = stim.Circuit(get_circuit_str())

new_circuit = original_circuit.copy()

# Append verification block
# Measure weight 4 stabilizers
# Indices 0 to 35
ancilla_start = 45
ancilla_indices = []

for i in range(36):
    s = stabs[i]
    p = stim.PauliString(s)
    print(f"Stab {i} length: {len(p)}")
    
    ancilla = ancilla_start + i
    ancilla_indices.append(ancilla)
    
    # Init ancilla (Stim automatically inits 0? No, assume 0. But for safety R 0?)
    # "All ancilla qubits must be initialized in the |0> state"
    # The circuit is state prep, implies |0> start.
    # But these are new qubits.
    # We should add R gates? No, the prompt says "initialized in the |0> state".
    # Usually this means we assume they are 0.
    # But adding `R` (reset) is safe.
    # But `R` is not unitary?
    # "and measured at the end".
    # I'll rely on default |0>.
    
    # H ancilla
    new_circuit.append("H", [ancilla])
    
    # Controlled operations
    # Iterate over data qubits
    # We need to extract X/Y/Z on each qubit.
    
    # p is stim.PauliString
    # We can iterate over non-identity indices
    # But we also need the Pauli type.
    
    # Create list of targets for this stabilizer
    # To avoid hook errors accumulating, the order matters?
    # With one ancilla, any order is same for single fault propagation?
    # Yes.
    
    # Get Pauli on each qubit
    # p[k] returns 0(I), 1(X), 2(Y), 3(Z).
    
    for q in range(45):
        if q < len(p):
            pauli = p[q]
        else:
            pauli = 0 # Identity
            
        if pauli == 1: # X
            new_circuit.append("CX", [ancilla, q])
        elif pauli == 3: # Z
            new_circuit.append("CZ", [ancilla, q])
        elif pauli == 2: # Y
            new_circuit.append("CY", [ancilla, q])
            
    # H ancilla
    new_circuit.append("H", [ancilla])
    
    # Measure ancilla
    new_circuit.append("M", [ancilla])

# Output the circuit
