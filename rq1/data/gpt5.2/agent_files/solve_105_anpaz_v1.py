def check_stabilizers(stabilizers):
    # Load stabilizers from file
    print(f"Loading {len(stabilizers)} stabilizers...")
    
    # Parse stabilizers
    pauli_stabilizers = []
    for s in stabilizers:
        try:
            pauli_stabilizers.append(stim.PauliString(s))
        except Exception as e:
            print(f"Error parsing stabilizer {s}: {e}")
            return

    # Check for commutativity
    print("Checking commutativity...")
    anticommuting_pairs = []
    for i in range(len(pauli_stabilizers)):
        for j in range(i + 1, len(pauli_stabilizers)):
            if not pauli_stabilizers[i].commutes(pauli_stabilizers[j]):
                anticommuting_pairs.append((i, j))
    
    if anticommuting_pairs:
        print(f"Found {len(anticommuting_pairs)} anticommuting pairs!")
        for i, j in anticommuting_pairs:
            print(f"  {i} and {j} anticommute")
            s1 = stabilizers[i]
            s2 = stabilizers[j]
            s1_str = s1
            s2_str = s2
            print(f"  {i}: {s1_str}")
            print(f"  {j}: {s2_str}")
            
            # Print explicit indices of overlap
            print("  Overlap details:")
            for k in range(len(s1_str)):
                c1 = s1_str[k]
                c2 = s2_str[k]
                if c1 != 'I' and c2 != 'I':
                    commutes = (c1 == c2) or (c1 == 'I') or (c2 == 'I') 
                    # Actually valid check: X and Z anticommute, Y and Z anticommute, X and Y anticommute
                    # Same operator commutes.
                    anticomm = False
                    if (c1=='X' and c2=='Z') or (c1=='Z' and c2=='X'): anticomm = True
                    if (c1=='X' and c2=='Y') or (c1=='Y' and c2=='X'): anticomm = True
                    if (c1=='Y' and c2=='Z') or (c1=='Z' and c2=='Y'): anticomm = True
                    
                    status = "ANTICOMMUTES" if anticomm else "commutes"
                    print(f"    Index {k}: {c1} vs {c2} -> {status}")

    else:
        print("All stabilizers commute.")

    # Try to generate circuit using tableau method
    print("Attempting to generate circuit using Tableau.from_stabilizers...")
    try:
        tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
        circuit = tableau.to_circuit("elimination")
        print("Circuit generated successfully!")
        print(f"Circuit length: {len(circuit)}")
        
        # Verify circuit
        print("Verifying circuit...")
        sim = stim.TableauSimulator()
        sim.do(circuit)
        
        preserved = 0
        for i, s in enumerate(pauli_stabilizers):
            expectation = sim.peek_observable_expectation(s)
            if expectation == 1:
                preserved += 1
            else:
                print(f"Stabilizer {i} not preserved: {expectation}")
        
        print(f"Preserved {preserved}/{len(pauli_stabilizers)} stabilizers.")
        
        with open("circuit_105.stim", "w") as f:
            f.write(str(circuit))
            
    except Exception as e:
        print(f"Error generating circuit: {e}")

if __name__ == "__main__":
    check_stabilizers(stabilizers)
