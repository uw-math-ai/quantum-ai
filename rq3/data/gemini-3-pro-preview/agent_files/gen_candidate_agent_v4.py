try:
    pauli_stabilizers = [stim.PauliString(s) for s in stabilizers_str]
    # Use allow_redundant=True because the set might be overcomplete
    # Use allow_underconstrained=True because the set might not specify all 36 qubits fully (though it looks like a lot of stabilizers)
    tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_redundant=True, allow_underconstrained=True)
    
    # Synthesize using graph_state method (produces CZ gates, 0 CXs)
    circuit = tableau.to_circuit(method="graph_state")
    
    # Filter out Resets to comply with "no resets unless in baseline" rule
    # The baseline implies |0> input.
    clean_circuit = stim.Circuit()
    for instruction in circuit:
        if instruction.name in ["R", "RX", "RY", "RZ", "TICK"]:
            continue
        clean_circuit.append(instruction)
        
    with open("candidate_agent.stim", "w") as f:
        f.write(str(clean_circuit))
    print("Circuit written to candidate_agent.stim")

except Exception as e:
    print(f"Error: {e}")
