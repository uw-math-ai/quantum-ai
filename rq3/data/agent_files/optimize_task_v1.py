try:
    stabilizers = [stim.PauliString(s) for s in stabilizers_str]
    tableau = stim.Tableau.from_stabilizers(stabilizers, allow_underconstrained=True)
    # Generate graph state circuit
    circuit = tableau.to_circuit(method="graph_state")
    
    # Post-process: Replace RX with H (assuming start from |0>)
    # Graph state synthesis often puts RX at the beginning to prepare |+>
    # Since we start with |0>, H is equivalent.
    processed_circuit = stim.Circuit()
    for instruction in circuit:
        if instruction.name == "RX":
            processed_circuit.append("H", instruction.targets_copy())
        else:
            processed_circuit.append(instruction)
            
    print(processed_circuit)

except Exception as e:
    print(f"Error: {e}")
