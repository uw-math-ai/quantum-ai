def check_anticommute(error_paulis, stabilizer):
    # Count non-commuting terms
    # Pauli matrices: I=0, X=1, Z=2, Y=3.
    # Commutation: if P1, P2 commute.
    # {X, Z} = 0. {X, Y} = 0. {Y, Z} = 0.
    # {X, X} = 1. {Y, Y} = 1. {Z, Z} = 1.
    # Wait, anti-commute means {A, B} = 0 (AB = -BA).
    # Different Paulis (excluding I) anticommute. Same Paulis commute.
    # So if p != s_p and p!='I' and s_p!='I', they anticommute.
    # We count number of anticommuting positions.
    # If odd, they anticommute globally.
    
    count = 0
    for idx_str, p in error_paulis.items():
        idx = int(idx_str)
        if idx >= len(stabilizer): continue
        s = stabilizer[idx]
        if s == 'I' or p == 'I': continue
        if s != p:
            count += 1
    return count % 2 == 1

