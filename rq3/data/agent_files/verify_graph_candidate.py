stabilizers = [s.strip() for s in stabilizers_str.strip().split('\n') if s.strip()]

def count_cx(circ):
    count = 0
    for inst in circ:
        if inst.name == "CX":
            count += len(inst.targets_copy()) // 2
    return count

def count_volume(circ):
    count = 0
    for inst in circ:
        if inst.name in ["CX", "CY", "CZ", "H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "H_XZ", "H_YZ", "H_XY", "S_DAG", "SQRT_X_DAG", "SQRT_Y_DAG", "SQRT_Z_DAG"]:
            if inst.name in ["CX", "CY", "CZ"]:
                count += len(inst.targets_copy()) // 2
            else:
                count += len(inst.targets_copy())
    return count

# Read candidate_graph.stim
with open("candidate_graph.stim", "r") as f:
    content = f.read()

# Replace RX with H
content = re.sub(r"^RX ", "H ", content, flags=re.MULTILINE)
# Remove TICK
content = re.sub(r"^TICK\n", "", content, flags=re.MULTILINE)

# Version 1: With CZ (0 CX)
circ_cz = stim.Circuit(content)
print("--- Version 1: With CZ ---")
print(f"CX Count: {count_cx(circ_cz)}")
print(f"Volume: {count_volume(circ_cz)}")

# Check validity
sim = stim.TableauSimulator()
sim.do(circ_cz)
preserved = 0
for s in stabilizers:
    if sim.peek_observable_expectation(stim.PauliString(s)) == 1:
        preserved += 1
print(f"Preserved: {preserved}/{len(stabilizers)}")

with open("candidate_cz.stim", "w") as f:
    f.write(str(circ_cz))

# Version 2: Convert CZ to CX
# CZ a b = H b CX a b H b
circ_cx = stim.Circuit()
for inst in circ_cz:
    if inst.name == "CZ":
        targets = inst.targets_copy()
        for i in range(0, len(targets), 2):
            c = targets[i]
            t = targets[i+1]
            circ_cx.append("H", [t])
            circ_cx.append("CX", [c, t])
            circ_cx.append("H", [t])
    else:
        circ_cx.append(inst)

print("--- Version 2: With CX ---")
print(f"CX Count: {count_cx(circ_cx)}")
print(f"Volume: {count_volume(circ_cx)}")

