stabilizers = [s.strip().replace(',', '') for s in stabilizers]

num_qubits = len(stabilizers[0])
print(f"Number of qubits: {num_qubits}")
print(f"Number of stabilizers: {len(stabilizers)}")

try:
    tableau = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in stabilizers], allow_underconstrained=True)
    print("Tableau creation successful.")
    
    # Synthesize circuit
    circuit_graph = tableau.to_circuit(method="graph_state")
    print("Graph state synthesis successful.")
    print(f"Graph state circuit length: {len(circuit_graph)}")
    print(f"CX count: {circuit_graph.num_cx}")
    print(f"CZ count: {circuit_graph.num_cz}")
    
    # Synthesize with elimination
    circuit_elim = tableau.to_circuit(method="elimination")
    print("Elimination synthesis successful.")
    print(f"Elimination circuit length: {len(circuit_elim)}")
    print(f"CX count: {circuit_elim.num_cx}")
    
    # Save best one
    with open("synthesized_graph.stim", "w") as f:
        f.write(str(circuit_graph))
    
    # Check stabilizer preservation
    sim = stim.TableauSimulator()
    sim.do(circuit_graph)
    preserved = 0
    for s in stabilizers:
        if sim.peek_observable_expectation(stim.PauliString(s)) == 1:
            preserved += 1
    print(f"Preserved stabilizers: {preserved}/{len(stabilizers)}")
    
except Exception as e:
    print(f"Error: {e}")

