def count_metrics(circuit):
    cx = 0
    vol = 0
    for instr in circuit:
        targets = instr.targets_copy()
        if instr.name in ["CX", "CNOT"]:
            cx += len(targets) // 2
            vol += len(targets) // 2
        elif instr.name in ["CY", "CZ", "H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "X", "Y", "Z"]:
             if instr.name in ["CX", "CY", "CZ"]:
                 vol += len(targets) // 2
             else:
                 vol += len(targets)
    return cx, vol

