def clean_circuit(circ):
    # Replace RX with H
    new_circ = stim.Circuit()
    for instruction in circ:
        if instruction.name == "RX":
            # RX resets to |+>. Since we start at |0>, H takes |0> to |+>.
            targets = instruction.targets_copy()
            new_circ.append("H", targets)
        else:
            new_circ.append(instruction)
    return new_circ

try:
    tableau = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in stabilizers], allow_underconstrained=True)
    circuit = tableau.to_circuit(method="graph_state")
    cleaned_circuit = clean_circuit(circuit)
    print(cleaned_circuit)
except Exception as e:
    print(f"Error: {e}")
