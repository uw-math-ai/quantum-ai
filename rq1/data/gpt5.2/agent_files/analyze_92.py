while remaining:
    # Pick node with min degree (heuristic for max independent set)
    # Actually, to MAXIMIZE independent set, we should pick nodes with min degree? 
    # Or remove max degree nodes?
    # Strategy: iteratively remove node with max degree until no edges.
    
    # Let's try removing max degree nodes until graph is empty
    temp_G = G.subgraph(list(remaining)).copy()
    if temp_G.number_of_edges() == 0:
        kept.extend(list(remaining))
        break
        
    # Remove max degree
    degrees = dict(temp_G.degree())
    max_node = max(degrees, key=degrees.get)
    remaining.remove(max_node)

