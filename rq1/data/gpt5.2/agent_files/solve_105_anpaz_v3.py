def solve():
    # Fix stabilizer 96 by shifting left by 2
    # Current: IIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIIZZIIIIIZZIIIIIIIIIIIIIII
    # We want: ...IIZZIIIIIZZIIIIIIIIIIIIIIIII...
    # Shifting 'ZZIIIIIZZ' part left by 2.
    
    s96 = stabilizers[96]
    # Identify the pattern 'ZZIIIIIZZ'
    start = s96.find('Z')
    if start == -1:
        print("Error: stabilizer 96 has no Z")
        return
    
    # Check if shifting left by 2 works
    # We will construct a new list of stabilizers
    new_stabilizers = list(stabilizers)
    
    # Create corrected 96
    # Original starts at 83. We want 81.
    # The pattern length is 9 ('ZZIIIIIZZ').
    # But wait, it's 'IIZZIIIIIZZ' (11) if we include Is.
    # Let's just manually shift Zs.
    
    chars = list(s96)
    # Clear old Zs
    for k in range(len(chars)):
        if chars[k] == 'Z':
            chars[k] = 'I'
            
    # Set new Zs
    # Old: 83, 84, 90, 91
    # New: 81, 82, 88, 89
    chars[81] = 'Z'
    chars[82] = 'Z'
    chars[88] = 'Z'
    chars[89] = 'Z'
    new_stabilizers[96] = "".join(chars)
    
    print("Stabilizer 96 fixed.")
    
    # Check commutativity
    pauli_stabilizers = [stim.PauliString(s) for s in new_stabilizers]
    anticommuting = False
    for i in range(len(pauli_stabilizers)):
        for j in range(i + 1, len(pauli_stabilizers)):
            if not pauli_stabilizers[i].commutes(pauli_stabilizers[j]):
                print(f"Still anticommuting: {i} and {j}")
                anticommuting = True
                
    if not anticommuting:
        print("All stabilizers commute now!")
        
        # Generate circuit
        print("Generating circuit...")
        tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
        circuit = tableau.to_circuit("elimination")
        
        with open("circuit_105.stim", "w") as f:
            f.write(str(circuit))
            
        print("Circuit saved to circuit_105.stim")

