# Convert to stim PauliString format
stim_stabs = []
for s in stabilizers:
    stim_stabs.append(stim.PauliString("+" + s.replace('I', '_')))

print("\nCreating tableau from stabilizers...")
try:
    tab = stim.Tableau.from_stabilizers(stim_stabs, allow_redundant=True, allow_underconstrained=True)
    print(f"Tableau created with {len(tab)} qubits")
    
    # Generate circuit using graph_state method (produces CZ gates, CX=0)
    circuit = tab.to_circuit(method='graph_state')
    print(f"\nGenerated circuit:")
    print(circuit)
    
    # Count gates
    cx_count = 0
    cz_count = 0
    h_count = 0
    s_count = 0
    total = 0
    for inst in circuit:
        name = inst.name
        num_targets = len(inst.targets_copy())
        if name in ['CX', 'CNOT', 'CZ']:
            count = num_targets // 2
        else:
            count = num_targets
        total += count
        if name in ['CX', 'CNOT']:
            cx_count += count
        elif name == 'CZ':
            cz_count += count
        elif name == 'H':
            h_count += count
        elif name in ['S', 'S_DAG']:
            s_count += count
    
    print(f"\nGate counts: CX={cx_count}, CZ={cz_count}, H={h_count}, S={s_count}, Total={total}")
    
    # Save circuit
    with open("data/claude-opus-4.6/agent_files/candidate_v1.stim", "w") as f:
        f.write(str(circuit))
    print("\nSaved to candidate_v1.stim")
        
except Exception as e:
    print(f"Error: {e}")
    import traceback
    traceback.print_exc()
