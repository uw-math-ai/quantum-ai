stabilizers = [s.strip() for s in stabilizers_str.strip().split('\n')]
print(f"Loaded {len(stabilizers)} stabilizers.")

circuit = stim.Circuit(baseline_circuit_str)

def count_cx(circ):
    count = 0
    for inst in circ:
        if inst.name == "CX":
            count += len(inst.targets_copy()) // 2
    return count

def count_volume(circ):
    # Volume: total gate count in volume gate set
    count = 0
    for inst in circ:
        if inst.name in ["CX", "CY", "CZ", "H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "H_XZ", "H_YZ", "H_XY", "S_DAG", "SQRT_X_DAG", "SQRT_Y_DAG", "SQRT_Z_DAG"]:
            if inst.name in ["CX", "CY", "CZ"]:
                count += len(inst.targets_copy()) // 2
            else:
                count += len(inst.targets_copy())
    return count

print(f"Baseline CX count: {count_cx(circuit)}")
print(f"Baseline Volume: {count_volume(circuit)}")

# 1. Verify baseline
sim = stim.TableauSimulator()
sim.do(circuit)
preserved = 0
for s in stabilizers:
    if sim.peek_observable_expectation(stim.PauliString(s)) == 1:
        preserved += 1
    else:
        print(f"Failed: {s}")

print(f"Preserved: {preserved}/{len(stabilizers)}")

if preserved != len(stabilizers):
    print("Baseline is INVALID! (or verification is wrong)")
else:
    print("Baseline is VALID.")

# 2. Synthesize from stabilizers
try:
    # Use allow_underconstrained=True
    tableau = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in stabilizers], allow_underconstrained=True)
    print("Tableau constructed from stabilizers (underconstrained).")
    
    # Method 1: Elimination
    synth_circ = tableau.to_circuit(method="elimination")
    print(f"Synthesis (elimination) CX: {count_cx(synth_circ)}")
    print(f"Synthesis (elimination) Vol: {count_volume(synth_circ)}")
    
    # Check elimination result
    sim3 = stim.TableauSimulator()
    sim3.do(synth_circ)
    p3 = 0
    for s in stabilizers:
        if sim3.peek_observable_expectation(stim.PauliString(s)) == 1:
            p3 += 1
    print(f"Elimination state preserved: {p3}/{len(stabilizers)}")
    
    if p3 == len(stabilizers):
        with open("candidate_elimination.stim", "w") as f:
            f.write(str(synth_circ))
        print("Saved candidate_elimination.stim")

    # Method 2: Graph state
    try:
        # Note: stim might not be able to convert to graph state if it's not one.
        # But we can try 'graph_state' method.
        synth_graph = tableau.to_circuit(method="graph_state")
        print(f"Synthesis (graph_state) CX: {count_cx(synth_graph)}")
        print(f"Synthesis (graph_state) Vol: {count_volume(synth_graph)}")
        
        sim2 = stim.TableauSimulator()
        sim2.do(synth_graph)
        p2 = 0
        for s in stabilizers:
            if sim2.peek_observable_expectation(stim.PauliString(s)) == 1:
                p2 += 1
        print(f"Graph state preserved: {p2}/{len(stabilizers)}")
        if p2 == len(stabilizers):
            with open("candidate_graph.stim", "w") as f:
                f.write(str(synth_graph))
            print("Saved candidate_graph.stim")
            
    except Exception as e:
        print(f"Graph state synthesis failed: {e}")

except Exception as e:
    print(f"Tableau construction failed: {e}")
