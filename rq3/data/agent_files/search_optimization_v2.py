def count_metrics(circuit):
    cx_count = 0
    volume = 0
    for instr in circuit:
        if instr.name == "CX":
            cx_count += len(instr.targets_copy()) // 2
            volume += len(instr.targets_copy())
        elif instr.name in ["H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "X", "Y", "Z"]:
             volume += len(instr.targets_copy())
        elif instr.name in ["CZ", "SWAP"]:
             volume += len(instr.targets_copy())
    return cx_count, volume

def apply_permutation(stabilizers, perm):
    # perm is list where perm[i] is the physical qubit for logical qubit i
    n = len(stabilizers[0])
    new_stabilizers = []
    for s in stabilizers:
        new_chars = ['I'] * n
        for i, char in enumerate(s):
            # i is logical index.
            # char moves to physical index perm[i].
            new_chars[perm[i]] = char
        new_stabilizers.append("".join(new_chars))
    return new_stabilizers

def inverse_permutation_circuit(circuit, perm):
    # perm maps logical -> physical.
    # The circuit acts on physical qubits.
    # We want to map physical qubits back to logical qubits.
    # If physical is `p`, then logical is `perm.index(p)`.
    
    perm_inv = {v: k for k, v in enumerate(perm)}
    
    new_circuit = stim.Circuit()
    for instr in circuit:
        new_targets = []
        for t in instr.targets_copy():
            if t.is_qubit_target:
                # t.value is physical index
                new_targets.append(stim.target_rec(perm_inv[t.value]))
            else:
                new_targets.append(t)
        new_circuit.append(instr.name, new_targets, instr.gate_args_copy())
    return new_circuit

def run_search():
    best_cx = 662
    best_vol = 1373
    best_circuit = None
    
    n_qubits = len(stabilizers[0])
    
    print(f"Starting search. Baseline: {best_cx} CX, {best_vol} Vol")
    
    # Try 1000 iterations
    for i in range(1000):
        # 1. Shuffle order of stabilizers
        current_stabilizers = stabilizers[:]
        random.shuffle(current_stabilizers)
        
        # 2. Permute qubits
        perm = list(range(n_qubits))
        random.shuffle(perm)
        
        permuted_stabs = apply_permutation(current_stabilizers, perm)
        
        try:
            tableau = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in permuted_stabs], allow_underconstrained=True)
            circ = tableau.to_circuit("elimination")
            
            # Un-permute
            final_circ = inverse_permutation_circuit(circ, perm)
            
            cx, vol = count_metrics(final_circ)
            
            if cx < best_cx or (cx == best_cx and vol < best_vol):
                print(f"Iter {i}: Found better! CX={cx}, Vol={vol}")
                best_cx = cx
                best_vol = vol
                best_circuit = final_circ
                with open("candidate_circuit.stim", "w") as f:
                    f.write(str(best_circuit))
            
            if i % 50 == 0:
                print(f"Iter {i}...", end='\r')
                
        except Exception as e:
            # print(e)
            pass

    if best_circuit:
        print("\nSearch finished. Found improvement.")
    else:
        print("\nSearch finished. No improvement found.")

