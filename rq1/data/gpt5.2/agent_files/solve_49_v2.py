# Find commuting subset to build tableau
def commutes(s1, s2):
    count = 0
    for p1, p2 in zip(s1, s2):
        if p1 != 'I' and p2 != 'I' and p1 != p2:
            count += 1
    return count % 2 == 0

# Find max commuting subset with greedy
selected = []
for i, s in enumerate(stabilizers_raw):
    if all(commutes(s, stabilizers_raw[j]) for j in selected):
        selected.append(i)

print(f"Found {len(selected)} commuting stabilizers")

# Use these for tableau
stab_list = [stim.PauliString(stabilizers_raw[i]) for i in selected]

# Create tableau 
tableau = stim.Tableau.from_stabilizers(stab_list, allow_underconstrained=True)

# Generate circuit using graph_state method (optimal for CZ)
circuit_raw = tableau.to_circuit(method="graph_state")
print(f"Raw circuit: {len(circuit_raw)} instructions")

# Clean up: RX -> H (since we start from |0>)
circuit_lines = str(circuit_raw).strip().split('\n')
cleaned_lines = []
for line in circuit_lines:
    if line.startswith('RX'):
        # Replace RX with H
        cleaned_lines.append(line.replace('RX', 'H'))
    elif line.strip() and not line.startswith('TICK'):
        cleaned_lines.append(line)

circuit_str = '\n'.join(cleaned_lines)
print("\nCleaned circuit:")
print(circuit_str)

# Save circuit
with open('data/gpt5.2/agent_files/candidate_49.stim', 'w') as f:
    f.write(circuit_str)
