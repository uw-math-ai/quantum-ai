# Build a graph and find maximal independent set (or greedy approach)
# The graph has edges for anticommuting stabilizers
from collections import defaultdict

conflicts = defaultdict(set)
for i, j in anticom_pairs:
    conflicts[i].add(j)
    conflicts[j].add(i)

# Greedy: prioritize stabilizers with fewer conflicts
remaining = set(range(len(stabilizers)))
selected = []

while remaining:
    # Find the stabilizer with fewest conflicts in remaining
    best = min(remaining, key=lambda x: len(conflicts[x] & remaining))
    selected.append(best)
    # Remove this stabilizer and its conflicts from remaining
    to_remove = conflicts[best] & remaining
    remaining -= {best}
    remaining -= to_remove

print(f"\nSelected {len(selected)} commuting stabilizers")
print(f"Dropped: {len(stabilizers) - len(selected)}")

# Create PauliStrings for selected stabilizers
selected_stabs = [stim.PauliString(stabilizers[i]) for i in sorted(selected)]

# Verify they all commute
all_commute = True
for i in range(len(selected_stabs)):
    for j in range(i+1, len(selected_stabs)):
        if not selected_stabs[i].commutes(selected_stabs[j]):
            all_commute = False
            print(f"Still anticommuting: {i} vs {j}")
