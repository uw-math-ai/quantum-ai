# Group by type (X or Z) if possible, but they are mixed.
# But looking at the list, the first half seem to be X-ish and the second half Z-ish.
# Actually, let's just assume the 4-block structure and analyze the first block (first 19 qubits).

patterns = {}
multi_block_stabs = []

for i, s in enumerate(stabs):
    sub_patterns = []
    blocks_active = []
    
    for b in range(4):
        chunk = s[b*19 : (b+1)*19]
        if chunk != "I" * 19:
            blocks_active.append(b)
            sub_patterns.append(chunk)

    if len(blocks_active) == 1:
        # Single block
        b = blocks_active[0]
        pat = sub_patterns[0]
        if pat not in patterns:
            patterns[pat] = []
        patterns[pat].append(b)
    else:
        # Multi block
        multi_block_stabs.append((i, s, blocks_active, sub_patterns))

print(f"Number of single-block patterns: {len(patterns)}")
for p, blocks in patterns.items():
    print(f"Pattern '{p}' on blocks {blocks}")

print(f"\nNumber of multi-block stabilizers: {len(multi_block_stabs)}")
for i, s, blocks, subs in multi_block_stabs:
    print(f"Index {i}: Blocks {blocks}")
    print(f"  Subs: {subs}")
