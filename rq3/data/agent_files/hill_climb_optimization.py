def count_metrics(circuit):
    cx_count = 0
    volume = 0
    for instr in circuit:
        if instr.name == "CX":
            cx_count += len(instr.targets_copy()) // 2
            volume += len(instr.targets_copy())
        elif instr.name in ["H", "S", "SQRT_X", "SQRT_Y", "SQRT_Z", "X", "Y", "Z"]:
             volume += len(instr.targets_copy())
        elif instr.name in ["CZ", "SWAP"]:
             volume += len(instr.targets_copy())
    return cx_count, volume

def apply_permutation(stabilizers, perm):
    n = len(stabilizers[0])
    new_stabilizers = []
    for s in stabilizers:
        new_chars = ['I'] * n
        for i, char in enumerate(s):
            new_chars[perm[i]] = char
        new_stabilizers.append("".join(new_chars))
    return new_stabilizers

def inverse_permutation_circuit(circuit, perm):
    perm_inv = {v: k for k, v in enumerate(perm)}
    new_circuit = stim.Circuit()
    for instr in circuit:
        new_targets = []
        for t in instr.targets_copy():
            if t.is_qubit_target:
                new_targets.append(perm_inv[t.value])
            else:
                new_targets.append(t)
        new_circuit.append(instr.name, new_targets, instr.gate_args_copy())
    return new_circuit

def get_metrics_for_perm(perm):
    permuted_stabs = apply_permutation(stabilizers, perm)
    tableau = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in permuted_stabs], allow_underconstrained=True)
    circ = tableau.to_circuit("elimination")
    final_circ = inverse_permutation_circuit(circ, perm)
    cx, vol = count_metrics(final_circ)
    return cx, vol, final_circ

def hill_climb():
    best_cx = 662
    best_vol = 1373
    best_circuit = None
    
    n_qubits = len(stabilizers[0])
    current_perm = list(range(n_qubits))
    
    print(f"Starting hill climb. Baseline: {best_cx} CX, {best_vol} Vol")
    
    # Try multiple restarts
    for restart in range(5):
        random.shuffle(current_perm)
        current_cx, current_vol, current_circ = get_metrics_for_perm(current_perm)
        print(f"Restart {restart}: CX={current_cx}, Vol={current_vol}")
        
        for step in range(200):
            # Try a random swap
            new_perm = current_perm[:]
            idx1, idx2 = random.sample(range(n_qubits), 2)
            new_perm[idx1], new_perm[idx2] = new_perm[idx2], new_perm[idx1]
            
            try:
                cx, vol, circ = get_metrics_for_perm(new_perm)
                
                # Accept if strictly better or equal (to drift)
                if cx <= current_cx:
                    if cx < current_cx or vol <= current_vol:
                        current_perm = new_perm
                        current_cx = cx
                        current_vol = vol
                        current_circ = circ
                        
                        if cx < best_cx or (cx == best_cx and vol < best_vol):
                            print(f"Improvement! CX={cx}, Vol={vol}")
                            best_cx = cx
                            best_vol = vol
                            best_circuit = circ
                            with open("candidate_circuit.stim", "w") as f:
                                f.write(str(best_circuit))
            except Exception:
                pass
                
    if best_circuit:
        print("\nHill climb finished. Found improvement.")
