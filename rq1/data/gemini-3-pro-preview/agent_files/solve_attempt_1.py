def solve_circuit(stabilizers):
    num_qubits = len(stabilizers[0])
    
    print(f"Number of stabilizers: {len(stabilizers)}")
    print(f"Number of qubits: {num_qubits}")
    
    try:
        pauli_strings = [stim.PauliString(s) for s in stabilizers]
        
        if len(stabilizers) == num_qubits:
             tableau = stim.Tableau.from_stabilizers(pauli_strings)
             circuit = tableau.to_circuit()
             print(circuit)
             return

        # If we have fewer stabilizers than qubits, we can try to complete the set.
        if len(stabilizers) < num_qubits:
            print(f"Under-constrained: {len(stabilizers)} stabilizers for {num_qubits} qubits.")
            # Try to add a simple Z or X stabilizer to complete the set
            for i in range(num_qubits):
                for p in ['Z', 'X']:
                    candidate_str = ['I'] * num_qubits
                    candidate_str[i] = p
                    candidate = "".join(candidate_str)
                    
                    test_stabilizers = pauli_strings + [stim.PauliString(candidate)]
                    try:
                        # stim.Tableau.from_stabilizers checks for independence and commutation
                        # If it succeeds, we found a valid completion
                        tableau = stim.Tableau.from_stabilizers(test_stabilizers)
                        print(f"Found completion with {p} on qubit {i}")
                        circuit = tableau.to_circuit()
                        print(circuit)
                        return
                    except Exception as inner_e:
                        # Continue if this candidate doesn't work (dependent or anticommutes)
                        continue
            
            print("Could not find a simple single-qubit completion.")
        
    except Exception as e:
        print(f"Error: {e}")

