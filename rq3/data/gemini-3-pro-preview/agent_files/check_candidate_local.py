with open("candidate.stim", "r") as f:
    circuit_str = f.read()

circuit = stim.Circuit(circuit_str)
sim = stim.TableauSimulator()
sim.do(circuit)

valid = True
for s in stabilizers:
    if sim.peek_observable_expectation(stim.PauliString(s)) != 1:
        print(f"Failed stabilizer: {s}")
        valid = False
        break

if valid:
    print("Candidate preserves all stabilizers.")
else:
    print("Candidate does NOT preserve all stabilizers.")

