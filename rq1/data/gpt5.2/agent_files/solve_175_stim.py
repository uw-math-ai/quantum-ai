stabilizers = []

# Block 1: XXIIXX pattern (25 stabilizers, shift by 7)
# Pattern: XX II XX at positions i*7, i*7+1, i*7+4, i*7+5
for i in range(25):
    s = ['I'] * N
    base = i * 7
    s[base] = 'X'
    s[base + 1] = 'X'
    s[base + 4] = 'X'
    s[base + 5] = 'X'
    stabilizers.append(''.join(s))

# Block 2: XIXIXIXI pattern (26 stabilizers, shift by 7)
