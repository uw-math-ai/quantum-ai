# Get stabilizers that only touch one block
for block_idx in range(7):
    block_start = block_idx * 7
    block_end = block_start + 7
    local_stabs = []
    for s in stabs:
        # Check if stabilizer only acts on this block
        non_id = [i for i, c in enumerate(s) if c != 'I']
        if all(block_start <= i < block_end for i in non_id):
            local_stabs.append(s)
    print(f"Block {block_idx} (qubits {block_start}-{block_end-1}): {len(local_stabs)} local stabilizers")

