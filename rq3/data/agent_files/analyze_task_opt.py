    def check_stabilizers(circuit, stabilizers):
        sim = stim.TableauSimulator()
        sim.do(circuit)
        
        preserved = 0
        for s_str in stabilizers:
            p = stim.PauliString(s_str)
            # expectation: +1 -> stabilized, -1 -> anti-stabilized, 0 -> random
            if sim.peek_observable_expectation(p) == 1:
                preserved += 1
        return preserved

    preserved_count = check_stabilizers(decomposed_graph, stabilizers)
