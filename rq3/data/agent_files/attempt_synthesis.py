try:
    # Convert strings to PauliStrings
    pauli_stabilizers = [stim.PauliString(s) for s in stabilizers]
    
    # Create tableau
    tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
    print(f"Successfully created tableau from {len(stabilizers)} stabilizers.")
    
    # Try graph state synthesis
    circuit_graph = tableau.to_circuit(method="graph_state")
    print("\nGraph State Circuit:")
    print(circuit_graph)

    # Check for RX gates and CX count
    print(f"\nCX count: {circuit_graph.num_cx}")
    print(f"CZ count: {circuit_graph.num_cz}")
    print(f"Total gates: {len(circuit_graph)}")
    
except Exception as e:
    print(f"Error: {e}")
