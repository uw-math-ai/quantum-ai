# Parse and extract CX patterns
circuit = stim.Circuit(baseline)

# Get all CX pairs
cx_pairs = []
for inst in circuit:
    if inst.name == 'CX':
        targets = inst.targets_copy()
        for i in range(0, len(targets), 2):
            cx_pairs.append((targets[i].value, targets[i+1].value))

print(f'Total CX: {len(cx_pairs)}')

# Find consecutive identical pairs
cancels = []
i = 0
while i < len(cx_pairs) - 1:
    if cx_pairs[i] == cx_pairs[i+1]:
        print(f'Cancel at {i}: CX {cx_pairs[i][0]} {cx_pairs[i][1]}')
        cancels.append(i)
        i += 2
    else:
        i += 1

print(f'Total canceling pairs (in partial baseline): {len(cancels)}')
