# Analyze the structure - we have 5 blocks of 31 qubits = 155
# Each block has 15 generators
block_size = 31
n_blocks = 5

# Check if each block's X stabilizers are independent and commuting
# Stabilizers are organized per block:
# Block 0: qubits 0-30
# Block 1: qubits 31-61
# Block 2: qubits 62-92
# Block 3: qubits 93-123
# Block 4: qubits 124-154

# Look at the first stabilizer in each block
print("\nAnalyzing stabilizer structure per block...")
for block in range(n_blocks):
    start_qubit = block * block_size
    end_qubit = start_qubit + block_size
    print(f"\nBlock {block}: qubits {start_qubit}-{end_qubit-1}")
    
    # Find stabilizers that act only within this block
    block_stabs = []
    for i in x_only:
        s = all_stabs[i]
        # Check if non-identity only in this block
        active_in_block = s[start_qubit:end_qubit].replace('I', '')
        active_outside = (s[:start_qubit] + s[end_qubit:]).replace('I', '')
        if active_in_block and not active_outside:
            block_stabs.append(i)
    
    print(f"  X-only stabilizers in block: {len(block_stabs)}")
    
    # Do same for Z
    block_z_stabs = []
    for i in z_only:
        s = all_stabs[i]
        active_in_block = s[start_qubit:end_qubit].replace('I', '')
        active_outside = (s[:start_qubit] + s[end_qubit:]).replace('I', '')
        if active_in_block and not active_outside:
            block_z_stabs.append(i)
    
    print(f"  Z-only stabilizers in block: {len(block_z_stabs)}")

