    # Parse stabilizers
    stabilizers = [line.strip() for line in stabs_str.strip().split('\n')]
    
    # Create PauliStrings
    paulis = [stim.PauliString(s) for s in stabilizers]
    
    # Create Tableau
    try:
        tableau = stim.Tableau.from_stabilizers(paulis, allow_redundant=True, allow_underconstrained=True)
    except Exception as e:
        print(f"Error creating tableau: {e}", file=sys.stderr)
        return

    # Synthesize circuit
    circuit = tableau.to_circuit(method="graph_state")
    
    with open("candidate_raw.stim", "w") as f:
        f.write(str(circuit))
        
    final_circuit = stim.Circuit()
    for instr in circuit:
        if instr.name == "RX":
            # RX is reset to |+> state. Equivalent to H on |0>
            final_circuit.append(stim.CircuitInstruction("H", instr.targets_copy()))
        elif instr.name == "R":
            # R is reset to |0> state. If we assume |0> input, we can just drop it.
            pass
        elif instr.name == "RY":
             # RY is reset to |i> state. Equivalent to H S on |0>?
             # |i> = S |+> = S H |0>.
             # So replace RY with H then S.
            final_circuit.append(stim.CircuitInstruction("H", instr.targets_copy()))
            final_circuit.append(stim.CircuitInstruction("S", instr.targets_copy()))
        elif instr.name == "RZ":
            pass # Same as R
        else:
            final_circuit.append(instr)
    
    with open("candidate.stim", "w") as f:
        f.write(str(final_circuit))
    
    print("Written to candidate.stim")

