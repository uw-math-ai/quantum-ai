# Check for anticommuting pairs
def check_commutation(stabilizers):
    anticommuting_pairs = []
    for i in range(len(stabilizers)):
        for j in range(i+1, len(stabilizers)):
            if not stabilizers[i].commutes(stabilizers[j]):
                anticommuting_pairs.append((i, j))
    return anticommuting_pairs

pairs = check_commutation(pauli_strings)
if pairs:
    print(f"Found {len(pairs)} anticommuting pairs!")
    for p in pairs[:10]:
        print(f"  {p}")
else:
    print("All stabilizers commute!")

