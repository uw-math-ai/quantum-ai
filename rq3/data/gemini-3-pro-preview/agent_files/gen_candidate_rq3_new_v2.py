    tableau = stim.Tableau.from_stabilizers(stabilizers, allow_redundant=True, allow_underconstrained=True)
    circuit = tableau.to_circuit(method="graph_state")
    
    # Check for RX gates which are disallowed resets in this context (usually)
    # The baseline doesn't use R or M, just H, CX.
    # The prompt says: "Do NOT introduce measurements, resets, or noise unless they already exist in the baseline"
    # graph_state synthesis often produces circuits with resets if it thinks they are needed.
    # However, for a pure stabilizer state, it should be unitary.
    # But sometimes it uses RX to init |+> or RZ to init |0>.
    # Stim's graph_state produces a circuit starting from Reset, H, S, CZ.
    # We want a circuit that maps |0> to the state. 
    # The graph_state method produces a circuit that PREPARES the state.
    # It might include R gates (resets).
    # If it includes R gates, we should replace them with appropriate init if we assume input is |0>.
    # Since we are optimizing a circuit, we assume the input is |0...0>.
    # If the circuit starts with R (reset), we can just remove it if we know the input is fresh |0>.
    # Or replace R with nothing.
    
    # Let's clean the circuit
    clean_circuit = stim.Circuit()
    for instruction in circuit:
        if instruction.name == "R" or instruction.name == "RX" or instruction.name == "RY" or instruction.name == "RZ":
             # If resetting to 0, and we are already at 0, do nothing.
             pass
        elif instruction.name == "M" or instruction.name == "MX" or instruction.name == "MY" or instruction.name == "MZ":
             # Measurements not allowed? "Do NOT introduce measurements... unless they already exist"
             # If graph state synthesis adds measurements, it's usually for delayed choice or something.
             # But for pure stabilizer states it shouldn't.
             clean_circuit.append(instruction)
        else:
             clean_circuit.append(instruction)
             
    # One more thing: graph_state synthesis might output H on all qubits at start.
    # If we removed resets, we still have the H gates.
    
    print(clean_circuit)
    
