best_cx = 300 # Baseline is 294, we want STRICT improvement
best_vol = 1000 # Baseline is 318
best_circuit = None

print("Starting optimization...")

# Try shuffling stabilizers
for i in range(1000):
    random.shuffle(pauli_stabilizers)
    try:
        # Note: from_stabilizers output might depend on order if the set is underconstrained
        # But here it is fully constrained or we use allow_underconstrained=True
        tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
        circuit = tableau.to_circuit(method="elimination")
        cx, vol = count_metrics(circuit)
        
        if cx < best_cx or (cx == best_cx and vol < best_vol):
            best_cx = cx
            best_vol = vol
            best_circuit = circuit
            print(f"Found better: CX={cx}, Vol={vol}")
            with open("candidate_best.stim", "w") as f:
                f.write(str(circuit))

    except Exception as e:
        continue

