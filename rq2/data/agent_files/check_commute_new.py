def check(op_str, stabs):
    commutes_all = True
    for i, s in enumerate(stabs):
        # Check commutation
        anticommutes_count = 0
        for j in range(len(s)):
            p1 = s[j]
            p2 = op_str[j]
            # X, Z anticommute. I commutes with all. Same commute.
            if p1 != 'I' and p2 != 'I' and p1 != p2:
                anticommutes_count += 1
        
        if anticommutes_count % 2 != 0:
            commutes_all = False
            # print(f"Anticommutes with stab {i}: {s}")
            
    return commutes_all

