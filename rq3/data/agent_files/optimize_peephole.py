def commute(op1, op2):
    # Very simple commutation rules
    if op1.name == op2.name:
        if op1.name == "CX":
            # CX c t, CX c' t'
            # Disjoint: Yes
            # c=c': Yes (Control-Control)
            # t=t': Yes (Target-Target)
            # c=t' or t=c': No
            t1 = op1.targets_copy()
            t2 = op2.targets_copy()
            c1, tgt1 = t1[0].value, t1[1].value
            c2, tgt2 = t2[0].value, t2[1].value
            if c1 == tgt2 or tgt1 == c2:
                return False
            return True
        elif op1.name == "H":
            return True # Single qubit gates commute if disjoint. But we check disjoint below.
    
    # Check disjoint
    q1 = set(t.value for t in op1.targets_copy())
    q2 = set(t.value for t in op2.targets_copy())
    if q1.isdisjoint(q2):
        return True
        
    return False

def is_inverse(op1, op2):
    if op1.name != op2.name:
        return False
    if op1.targets_copy() != op2.targets_copy():
        return False
    # H, CX are self-inverse
    if op1.name in ["H", "CX", "CZ", "SWAP"]:
        return True
    return False

def optimize_circuit(circuit):
    # Flatten loop-free circuit into list of single-op instructions
    # Expand multi-target H?
    ops = []
    for instr in circuit:
        if instr.name in ["H", "S", "X", "Y", "Z"]:
            for t in instr.targets_copy():
                ops.append(stim.Circuit(f"{instr.name} {t.value}")[0])
        elif instr.name in ["CX", "CNOT", "CZ", "SWAP"]:
             targets = instr.targets_copy()
             for i in range(0, len(targets), 2):
                 t1 = targets[i]
                 t2 = targets[i+1]
                 ops.append(stim.Circuit(f"{instr.name} {t1.value} {t2.value}")[0])
        else:
            ops.append(instr)
            
    # Optimization pass
    # We maintain a list of active ops.
    # For each new op, we try to bubble it down from the top?
    # Or simplified: greedy cancellation.
    # Try to cancel ops[i] with ops[i+1].
    # If not, try to swap ops[i] with ops[i+1] if they commute, to see if ops[i] can cancel with ops[i+2].
    
    changed = True
    while changed:
        changed = False
        i = 0
        while i < len(ops) - 1:
            op1 = ops[i]
            op2 = ops[i+1]
            
            if is_inverse(op1, op2):
                # Cancel
                del ops[i]
                del ops[i] # Index shifts
                changed = True
                # Don't increment i, check new neighbors
                if i > 0: i -= 1
            elif commute(op1, op2):
                # Swap to see if it helps later?
                # Only swap if it brings op1 closer to another op1?
                # Or just verify commutation.
                # To implement "move as far as possible", we can scan ahead.
                
                # Look ahead for inverse of op1
                found_inverse = False
                for k in range(i+1, len(ops)):
                    opk = ops[k]
                    if is_inverse(op1, opk):
                        # Can we move op1 to opk?
                        # Check if op1 commutes with all between i+1 and k
                        can_move = True
                        for m in range(i+1, k):
                            if not commute(op1, ops[m]):
                                can_move = False
                                break
                        if can_move:
                            # Remove op1 and opk
                            del ops[k]
                            del ops[i]
                            changed = True
                            found_inverse = True
                            if i > 0: i -= 1
                            break
                    if not commute(op1, opk):
                        # Blocked
                        break
                
                if not found_inverse:
                    i += 1
            else:
                i += 1
                
    # Reconstruct circuit
    new_circ = stim.Circuit()
    for op in ops:
        new_circ.append(op)
    return new_circ

