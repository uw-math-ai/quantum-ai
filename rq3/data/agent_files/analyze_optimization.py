def get_metrics(circuit):
    cx = 0
    vol = 0
    for instr in circuit:
        if instr.name == "CX":
            cx += len(instr.targets_copy()) // 2
            vol += len(instr.targets_copy()) // 2
        elif instr.name in ["H", "S", "S_DAG", "SQRT_X", "SQRT_X_DAG", "SQRT_Y", "SQRT_Y_DAG", "CZ", "CY", "XC", "YC", "ZC"]:
             # Volume includes all 1q and 2q Clifford gates? 
             # Instructions say: volume = total gate count in volume gate set (CX, CY, CZ, H, S, SQRT_X, etc.)
             # The baseline only has CX and H (and maybe SWAPs which are 3 CXs usually).
             # The baseline has 'CX ...' and 'H ...'.
             # Let's count args.
             if instr.name in ["CX", "CY", "CZ"]:
                 vol += len(instr.targets_copy()) // 2
             else:
                 vol += len(instr.targets_copy())
    return cx, vol

