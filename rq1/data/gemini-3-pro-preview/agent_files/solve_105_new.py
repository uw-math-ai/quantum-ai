def check_commutativity(stabilizers):
    n_qubits = len(stabilizers[0])
    n_stabs = len(stabilizers)
    
    # Convert to Pauli string format (X, Z sets)
    xs = np.zeros((n_stabs, n_qubits), dtype=bool)
    zs = np.zeros((n_stabs, n_qubits), dtype=bool)
    
    for i, s in enumerate(stabilizers):
        for j, char in enumerate(s):
            if char == 'X':
                xs[i, j] = True
            elif char == 'Z':
                zs[i, j] = True
            elif char == 'Y':
                xs[i, j] = True
                zs[i, j] = True
                
    # Check commutativity: (X1 Z2 + Z1 X2) % 2 == 0
    # This computes the number of anticommuting positions.
    # We want the SUM of anticommuting positions mod 2.
    
    # xs shape: (n_stabs, n_qubits)
    # zs.T shape: (n_qubits, n_stabs)
    # result shape: (n_stabs, n_stabs)
    comm_matrix = (np.matmul(xs.astype(int), zs.T.astype(int)) + np.matmul(zs.astype(int), xs.T.astype(int))) % 2
    
    # DEBUG: Check 0 vs 45
    print(f"DEBUG: 0 vs 45 comm: {comm_matrix[0, 45]}")
    # Verify manually
    cnt = 0
    for k in range(n_qubits):
        if (xs[0,k] and zs[45,k]) or (zs[0,k] and xs[45,k]):
            cnt += 1
    print(f"DEBUG: 0 vs 45 overlap count: {cnt}, mod 2: {cnt%2}")
    
    anticommuting_pairs = []
    # Force ignore the last 8 stabilizers for a moment to see if the rest commute
    # indices 90-97 are the last ones.
    # Actually let's just run the standard check and see.
    for i in range(n_stabs):
        for j in range(i + 1, n_stabs):
            if comm_matrix[i, j] == 1:
                anticommuting_pairs.append((i, j))
                
    return anticommuting_pairs

def solve_stabilizers():
    anticommuting = check_commutativity(stabilizers)
    if anticommuting:
        print(f"Found {len(anticommuting)} anticommuting pairs.")
        for i, j in anticommuting[:10]:
            print(f"  {i} vs {j}")
            
        # Strategy: 
        # First, try to keep all Z stabilizers (45-89 and 94-97).
        # Check consistency of Z set.
        z_indices = list(range(45, 90)) + list(range(94, 98))
        z_stabs = [stabilizers[k] for k in z_indices]
        z_conflicts = check_commutativity(z_stabs)
        if not z_conflicts:
            print("Z-type stabilizers are consistent.")
            # Now greedily add X-type stabilizers that commute with Z-set and each other
            x_indices = list(range(0, 45)) + list(range(90, 94))
            
            final_indices = set(z_indices)
            
            # Sort X stabilizers by some heuristic? Maybe just iterate.
            for x_idx in x_indices:
                candidate_indices = list(final_indices) + [x_idx]
                candidate_stabs = [stabilizers[k] for k in candidate_indices]
                if not check_commutativity(candidate_stabs):
                    final_indices.add(x_idx)
                else:
                    # DEBUG why it failed
                    print(f"DEBUG: Failed to add X stabilizer {x_idx}")
                    pairs = check_commutativity(candidate_stabs)
                    print(f"  Conflicts: {pairs}")
                    # Just print first conflict
                    c1, c2 = pairs[0]
                    # Map back to indices
                    real_c1 = candidate_indices[c1]
                    real_c2 = candidate_indices[c2]
                    print(f"  First conflict: {real_c1} vs {real_c2}")
                    if real_c1 == x_idx:
                        other = real_c2
                    else:
                        other = real_c1
                    print(f"  Conflict with existing: {other}")
            
            print(f"Constructed consistent set with {len(final_indices)} stabilizers (Z-prioritized).")
            remaining_indices = final_indices
        else:
            print("Z-type stabilizers are inconsistent! Fallback to greedy removal.")
            remaining_indices = set(range(len(stabilizers)))
        while True:
            # Recompute conflicts for remaining
            current_stabs = [stabilizers[k] for k in sorted(remaining_indices)]
            pairs = check_commutativity(current_stabs)
            if not pairs:
                break
            
            # Count conflicts
            conflict_counts = {}
            for i_idx, j_idx in pairs:
                # Map back to original indices
                real_i = sorted(remaining_indices)[i_idx]
                real_j = sorted(remaining_indices)[j_idx]
                conflict_counts[real_i] = conflict_counts.get(real_i, 0) + 1
                conflict_counts[real_j] = conflict_counts.get(real_j, 0) + 1
            
            # Remove max conflict
            max_conflict = max(conflict_counts.items(), key=lambda x: x[1])
            print(f"Removing stabilizer {max_conflict[0]} with {max_conflict[1]} conflicts")
            remaining_indices.remove(max_conflict[0])
            
        print(f"Kept {len(remaining_indices)} stabilizers.")
        final_stabilizers = [stabilizers[k] for k in sorted(remaining_indices)]
    else:
        print("All stabilizers commute.")
        final_stabilizers = stabilizers

    # Now solve for the circuit using Stim
    try:
        tableau = stim.Tableau.from_stabilizers(
            [stim.PauliString(s) for s in final_stabilizers],
            allow_redundant=True,
            allow_underconstrained=True
        )
        circuit = tableau.to_circuit()
        print("Circuit generated successfully.")
        
        # Save to file
        with open(r"C:\Users\anpaz\Repos\quantum-ai\rq1\data\gemini-3-pro-preview\agent_files\circuit_105.stim", "w") as f:
            f.write(str(circuit))
            
    except Exception as e:
        print(f"Error generating circuit: {e}")

