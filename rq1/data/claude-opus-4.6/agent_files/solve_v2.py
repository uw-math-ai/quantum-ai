# Convert strings to PauliString objects
pauli_strings = [stim.PauliString(s) for s in stabilizers]

tableau = stim.Tableau.from_stabilizers(
    pauli_strings,
    allow_redundant=True,
    allow_underconstrained=True
)

# Get the graph_state circuit
circuit_gs = tableau.to_circuit(method='graph_state')
print("Graph state circuit:")
print(circuit_gs)

# Now convert RX to H (since RX|0> = |+> = H|0>)
# We need to start from |0> so RX should become H
circuit_str = str(circuit_gs)

# Replace RX with H at the beginning
lines = circuit_str.strip().split('\n')
new_lines = []
for line in lines:
    if line.startswith('RX '):
        # Replace RX with H
        qubits = line[3:]
        new_lines.append('H ' + qubits)
    else:
        new_lines.append(line)

circuit_clean = stim.Circuit('\n'.join(new_lines))
print("\nCleaned circuit (RX -> H):")
print(circuit_clean)

# Save the circuit
with open('data/claude-opus-4.6/agent_files/candidate_v2.stim', 'w') as f:
    f.write(str(circuit_clean))
