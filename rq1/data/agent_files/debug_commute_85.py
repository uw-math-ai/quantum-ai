def find_start(s):
    # first non-I character
    for i, c in enumerate(s):
        if c != 'I':
            return i
    return -1

def check_commutation(i, j):
    s1 = stim.PauliString(stabilizers[i])
    s2 = stim.PauliString(stabilizers[j])
    return s1.commutes(s2)

