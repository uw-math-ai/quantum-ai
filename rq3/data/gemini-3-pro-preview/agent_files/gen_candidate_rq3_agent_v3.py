pauli_stabilizers = [stim.PauliString(s) for s in target_stabilizers]

try:
    t = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_redundant=True, allow_underconstrained=True)
    c_graph = t.to_circuit(method="graph_state")

    for instruction in c_graph:
        if instruction.name == "TICK":
            continue
        
        name = instruction.name
        if name == "RX":
            name = "H"
        
        targets = instruction.targets_copy()
        # targets is a list of stim.GateTarget
        # We want the values (qubit indices)
        
        qubits = [t.value for t in targets]
        
        if name in ["CX", "CZ", "SWAP", "CNOT"]:
            # 2-qubit gates
            for i in range(0, len(qubits), 2):
                print(f"{name} {qubits[i]} {qubits[i+1]}")
        else:
            # 1-qubit gates (or others)
            # Print individually to be safe and clean
            for q in qubits:
                print(f"{name} {q}")

except Exception as e:
    import traceback
    traceback.print_exc()
