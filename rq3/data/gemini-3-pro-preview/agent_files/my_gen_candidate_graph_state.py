# Create tableau
tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_redundant=True, allow_underconstrained=True)

# Graph state synthesis
circuit = tableau.to_circuit(method='graph_state')

# Post-processing: Replace RX with H
new_circuit = stim.Circuit()
for instruction in circuit:
    if instruction.name == "RX":
        new_circuit.append("H", instruction.targets_copy())
    else:
        new_circuit.append(instruction)

with open("candidate.stim", "w") as f:
    for instr in new_circuit:
        # standard str(instr) usually keeps it on one line unless extremely long
        # but let's be safe and replace newlines with spaces for single instruction
        s = str(instr).replace('\n', ' ')
