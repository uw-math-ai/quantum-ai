# Create tableau from stabilizers
ps = [stim.PauliString(s) for s in stabs]
tableau = stim.Tableau.from_stabilizers(ps, allow_underconstrained=True)

# Convert tableau to circuit
circuit = tableau.to_circuit(method='graph_state')
print(f"Initial graph_state circuit:\n{circuit}")

# Count gates
def count_gates(circ):
    cx = 0
    vol = 0
    for inst in circ:
        name = inst.name
        if name in ['CX', 'CY', 'CZ']:
            cx += len(inst.targets_copy()) // 2
            vol += len(inst.targets_copy()) // 2
        elif name in ['H', 'S', 'SQRT_X', 'S_DAG', 'SQRT_X_DAG']:
            vol += len(inst.targets_copy())
    return cx, vol

cx, vol = count_gates(circuit)
print(f"CX count: {cx}, Volume: {vol}")
