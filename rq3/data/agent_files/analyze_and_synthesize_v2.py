stabilizers = [s.strip().replace(',', '') for s in stabilizers]

print(f"Number of stabilizers: {len(stabilizers)}")
tableau = stim.Tableau.from_stabilizers([stim.PauliString(s) for s in stabilizers], allow_underconstrained=True)
circuit = tableau.to_circuit(method="graph_state")

# Replace RX with H
new_circuit = stim.Circuit()
for instr in circuit:
    if instr.name == "RX":
        new_circuit.append("H", instr.targets_copy())
    else:
        new_circuit.append(instr)

print(f"Synthesized circuit length: {len(new_circuit)}")

# Count CX
cx_count = 0
for instr in new_circuit:
    if instr.name in ["CX", "CNOT"]:
        cx_count += 1
print(f"CX count: {cx_count}")

# Check preservation
sim = stim.TableauSimulator()
sim.do(new_circuit)
failed = 0
for s in stabilizers:
    if sim.peek_observable_expectation(stim.PauliString(s)) != 1:
        failed += 1

print(f"Failed stabilizers: {failed}")

with open("synthesized_candidate.stim", "w") as f:
    f.write(str(new_circuit))
