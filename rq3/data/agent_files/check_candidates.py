def check_stabilizers(circuit, stabilizers):
    sim = stim.TableauSimulator()
    sim.do_circuit(circuit)
    
    preserved = 0
    total = len(stabilizers)
    
    for stab_str in stabilizers:
        if not stab_str.strip(): continue
        pauli = stim.PauliString(stab_str)
        if sim.peek_observable_expectation(pauli) == 1:
            preserved += 1
            
    return preserved, total

def get_metrics(circuit):
    cx = 0
    volume = 0
    for instr in circuit:
        if instr.name in ["CX", "CNOT"]:
            cx += len(instr.targets_copy()) // 2
            volume += len(instr.targets_copy()) // 2
        elif instr.name in ["CZ"]:
            volume += len(instr.targets_copy()) // 2
        elif instr.name in ["H", "S", "X", "Y", "Z", "I"]:
            volume += len(instr.targets_copy())
        # Add other gates as needed
    return cx, volume

