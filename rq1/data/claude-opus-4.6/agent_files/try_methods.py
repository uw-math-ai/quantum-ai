pauli_strings = [stim.PauliString(s) for s in stabilizer_strings]

# Try different synthesis methods
methods = ['elimination', 'graph_state']
best_circuit = None
best_total = float('inf')

for method in methods:
    try:
        tableau = stim.Tableau.from_stabilizers(
            pauli_strings,
            allow_redundant=True,
            allow_underconstrained=True
        )
        circuit = tableau.to_circuit(method=method)
        
        # Clean up RX -> H
        lines = str(circuit).strip().split('\n')
        new_lines = []
        for line in lines:
            if line.startswith("RX "):
                qubits = line[3:]
                new_lines.append(f"H {qubits}")
            elif line.startswith("RY "):
                # RY resets to |+i>, which is H*S starting from |0>
                qubits = line[3:]
                new_lines.append(f"H {qubits}")
                new_lines.append(f"S {qubits}")
            elif line.startswith("R "):
                # R resets to |0>, which is identity from |0>
                continue
            else:
                new_lines.append(line)
        
        clean_text = '\n'.join(new_lines)
        clean_circuit = stim.Circuit(clean_text)
        
        # Count gates
        total = 0
        cx = 0
        cz = 0
        for inst in clean_circuit:
            name = inst.name
            if name == "TICK":
                continue
            num_targets = len(inst.targets_copy())
            if name == "CX" or name == "CNOT":
                cx += num_targets // 2
                total += num_targets // 2
            elif name == "CZ":
                cz += num_targets // 2
                total += num_targets // 2
            else:
                total += num_targets
        
        print(f"Method {method}: CX={cx}, CZ={cz}, Total={total}")
        
        if total < best_total:
            best_total = total
            best_circuit = clean_text
            print(f"  -> New best!")
            
    except Exception as e:
        print(f"Method {method} failed: {e}")

print(f"\nBest circuit (total={best_total}):")
print(best_circuit)

