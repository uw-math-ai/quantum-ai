def solve():
    try:
        print("Creating tableau...")
        # Convert strings to PauliStrings
        pauli_stabilizers = [stim.PauliString(s) for s in stabilizers]
        tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True, allow_redundant=True)
        print("Synthesizing circuit...")
        circuit = tableau.to_circuit(method="graph_state")
        print("Cleaning circuit...")
        
        new_circuit = stim.Circuit()
        for instruction in circuit:
            if instruction.name == "RX":
                # RX q means reset to |+>. H * |0> = |+>.
                for t in instruction.targets_copy():
                    # t is a GateTarget. For RX it should be a simple qubit target.
                    # We can use t.value for the qubit index.
                    new_circuit.append("H", [t.value])
            elif instruction.name == "R" or instruction.name == "RZ":
                 # R is Reset Z. Identity on |0>.
                 pass
            elif instruction.name == "TICK":
                pass
            elif instruction.name == "I":
                pass
            else:
                new_circuit.append(instruction)
        
        print("Done.")
        print(new_circuit)
        
    except Exception as e:
        import traceback
        traceback.print_exc()
        print(f"Error: {e}")

if __name__ == "__main__":
    solve()
