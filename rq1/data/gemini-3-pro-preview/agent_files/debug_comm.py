print(f"S{idx2}: {s2}")

anticomms = 0
for i in range(len(s1)):
    p1 = s1[i]
    p2 = s2[i]
    if p1 != 'I' and p2 != 'I' and p1 != p2:
        print(f"  Col {i}: {p1} vs {p2} (AC)")
        anticomms += 1
    elif p1 != 'I' and p2 != 'I':
        print(f"  Col {i}: {p1} vs {p2} (Comm)")

print(f"Total anticommutations: {anticomms}")
if anticomms % 2 == 1:
    print("RESULT: ANTICOMMUTE")
else:
    print("RESULT: COMMUTE")
