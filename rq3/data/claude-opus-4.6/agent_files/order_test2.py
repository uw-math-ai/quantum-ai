def count_cx(circuit):
    return sum(len(inst.targets_copy())//2 for inst in circuit if inst.name == 'CX')

def count_vol(circuit):
    vol = 0
    vol_gates = {'CX', 'CY', 'CZ', 'H', 'S', 'S_DAG', 'SQRT_X', 'SQRT_X_DAG', 'SQRT_Y', 'SQRT_Y_DAG'}
    for inst in circuit:
        if inst.name in vol_gates:
            if inst.name in ['CX', 'CY', 'CZ']:
                vol += len(inst.targets_copy()) // 2
            else:
                vol += len(inst.targets_copy())
    return vol

