def clean_stabilizers(stabilizers_str):
    lines = [line.strip() for line in stabilizers_str.strip().split('\n') if line.strip()]
    return lines

def get_metrics(circuit):
    cx_count = 0
    volume = 0
    for instr in circuit:
        if instr.name == "CX" or instr.name == "CNOT":
            n = len(instr.targets_copy()) // 2
            cx_count += n
            volume += n  # 1 gate per pair? Volume usually counts total gates.
                         # But let's assume volume = total number of operations
        elif instr.name == "CZ":
             n = len(instr.targets_copy()) // 2
             # If CZ is native, it's 1 gate. If not, it's decomposed. 
             # The metric rule says: cx_count (primary), volume (secondary).
             # Volume = total gate count.
             # If I output CZ, does the evaluator count it as CX?
             # The evaluator likely counts "CX" instructions.
             pass 
        
        # Recalculate volume properly
    # Re-iterate for volume
    # "volume - total gate count in the volume gate set (CX, CY, CZ, H, S, SQRT_X, etc.)"
    # So I should just count instructions * number of ops?
    # Stim instructions can be SIMD. e.g. H 0 1 2 is 3 gates.
    
    real_volume = sum(len(instr.targets_copy()) // (2 if instr.name in ["CX", "CNOT", "CZ", "SWAP", "ISWAP"] else 1) for instr in circuit if instr.name in ["CX", "CNOT", "CZ", "H", "S", "SQRT_X", "X", "Y", "Z", "I", "S_DAG", "SQRT_X_DAG", "SQRT_Y", "SQRT_Y_DAG"])
    
    # Recalculate CX count specifically
    real_cx = 0
    for instr in circuit:
        if instr.name == "CX" or instr.name == "CNOT":
            real_cx += len(instr.targets_copy()) // 2
            
    return real_cx, real_volume

