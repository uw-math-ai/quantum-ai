stabilizers = stabilizers_raw.strip().split('\n')
pauli_strings = [stim.PauliString(s) for s in stabilizers]

tableau = stim.Tableau.from_stabilizers(pauli_strings, allow_redundant=True, allow_underconstrained=True)
circuit = tableau.to_circuit(method='graph_state')

# Convert RX to H and remove TICK
new_lines = []
for inst in circuit:
    name = inst.name
    if name == 'RX':
        targets = inst.targets_copy()
        new_lines.append(f'H {" ".join(str(t.value) for t in targets)}')
    elif name == 'TICK':
        continue
    else:
        targets = inst.targets_copy()
        if name in ['CZ', 'CX']:
            pairs = []
            for i in range(0, len(targets), 2):
                pairs.append(f'{targets[i].value} {targets[i+1].value}')
            new_lines.append(f'{name} {" ".join(pairs)}')
        else:
            new_lines.append(f'{name} {" ".join(str(t.value) for t in targets)}')

circuit_str = '\n'.join(new_lines)

# Verify with stim
test_circuit = stim.Circuit(circuit_str)
print(f'Valid stim circuit: {len(test_circuit)} instructions')

# Count gates
cx, cz, h, s, other = 0, 0, 0, 0, 0
for inst in test_circuit:
    name = inst.name
    tgts = inst.targets_copy()
    if name == 'CX':
        cx += len(tgts) // 2
    elif name == 'CZ':
        cz += len(tgts) // 2
    elif name == 'H':
        h += len(tgts)
    elif name in ['S', 'S_DAG']:
        s += len(tgts)
    elif name in ['X', 'Y', 'Z']:
        other += len(tgts)

print(f'CX={cx}, CZ={cz}, H={h}, S={s}, Other={other}')
print(f'Volume: {cx + cz + h + s + other}')

