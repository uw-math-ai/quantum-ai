def get_metrics(circuit):
    cx = 0
    vol = 0
    for instr in circuit:
        if instr.name == "CX":
            cx += len(instr.targets_copy()) // 2
            vol += len(instr.targets_copy()) // 2
        elif instr.name in ["H", "S", "S_DAG", "SQRT_X", "SQRT_X_DAG", "SQRT_Y", "SQRT_Y_DAG", "CZ", "CY", "XC", "YC", "ZC"]:
             if instr.name in ["CX", "CY", "CZ"]:
                 vol += len(instr.targets_copy()) // 2
             else:
                 vol += len(instr.targets_copy())
    return cx, vol

