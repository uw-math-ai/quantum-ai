def debug():
    with open("candidate_clean_fresh.stim", "r") as f:
        circuit_text = f.read()
    circuit = stim.Circuit(circuit_text)
    
    sim = stim.TableauSimulator()
    sim.do(circuit)
    
    failed = []
    for i, s_str in enumerate(STABILIZERS):
        s = stim.PauliString(s_str)
        exp = sim.peek_observable_expectation(s)
        if exp != 1:
            print(f"Stabilizer {i} failed: {s_str} (Expectation: {exp})")
            failed.append(i)
        else:
            # print(f"Stabilizer {i} passed")
            pass
            
    if not failed:
        print("All stabilizers preserved!")
    else:
        print(f"Failed {len(failed)} stabilizers: {failed}")

