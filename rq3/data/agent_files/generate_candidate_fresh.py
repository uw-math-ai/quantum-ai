try:
    pauli_stabilizers = [stim.PauliString(s) for s in stabilizers]
    tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
    circuit = tableau.to_circuit(method="graph_state")
    
    # Replace RX with H
    new_circuit = stim.Circuit()
    for instr in circuit:
        if instr.name == "RX":
            new_circuit.append("H", instr.targets_copy())
        elif instr.name == "TICK":
            continue # Skip ticks if you want, or keep them
        else:
            new_circuit.append(instr)
            
    # Write to file
    with open("candidate_clean.stim", "w") as f:
        f.write(str(new_circuit))
        
    print("Candidate written to candidate_clean.stim")
    
except Exception as e:
    print(f"Error: {e}")
