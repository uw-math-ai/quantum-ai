c = stim.Circuit(circ_text)
gates = Counter()
two_qubit_gates = ['CX', 'CZ', 'CNOT', 'SWAP', 'ISWAP']
for inst in c:
    if inst.name in two_qubit_gates:
        gates[inst.name] += len(inst.targets_copy()) // 2
    else:
        gates[inst.name] += len(inst.targets_copy())

print('Gate counts:')
for name, count in sorted(gates.items(), key=lambda x: -x[1]):
    print(f'  {name}: {count}')

total = sum(gates.values())
print(f'Total volume: {total}')
print(f'CX count: {gates.get("CX", 0)}')
