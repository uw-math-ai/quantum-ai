def check_commute(s1, s2):
    # Returns True if commute
    anti = 0
    for i in range(len(s1)):
        p1 = s1[i]
        p2 = s2[i]
        if p1 == 'I' or p2 == 'I': continue
        if p1 != p2: anti += 1
    return (anti % 2) == 0

