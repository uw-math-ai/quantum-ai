# 2. Optimization Logic

def count_cx(circuit):
    count = 0
    for op in circuit:
        if op.name == "CX" or op.name == "CNOT":
            count += len(op.targets_copy()) // 2
    return count

def count_volume(circuit):
    count = 0
    for op in circuit:
        if op.name in ["CX", "CNOT", "CY", "CZ", "SWAP"]:
             count += len(op.targets_copy()) // 2
        elif op.name in ["H", "S", "S_DAG", "SQRT_X", "SQRT_X_DAG", "X", "Y", "Z", "I", "SQRT_Y", "SQRT_Y_DAG"]:
             count += len(op.targets_copy())
    return count

