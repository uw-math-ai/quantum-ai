import stim
import re

with open("candidate_cz.stim", "r") as f:
    content = f.read()

# Parse content and filter out indices >= 56
# Content structure:
# H 0 1 ... 57
# CZ 0 1 2 3 ...
# ...

lines = content.strip().split('\n')
new_lines = []

for line in lines:
    parts = line.split()
    if not parts:
        continue
    gate = parts[0]
    args = parts[1:]
    
    new_args = []
    for arg in args:
        try:
            val = int(arg)
            if val < 56:
                new_args.append(str(val))
        except ValueError:
            # Maybe annotations or other stuff, keep it
            new_args.append(arg)
            
    if new_args:
        new_lines.append(f"{gate} {' '.join(new_args)}")

final_content = '\n'.join(new_lines)

with open("candidate_final.stim", "w") as f:
    f.write(final_content)

# Verify
circ = stim.Circuit(final_content)
print(f"Final circuit instructions: {len(circ)}")

