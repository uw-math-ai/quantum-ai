stabilizers_str = [line.strip() for line in raw_stabs.strip().split('\n') if line.strip()]

print(f"Number of stabilizers: {len(stabilizers_str)}")
print(f"Qubits per stabilizer: {len(stabilizers_str[0])}")

try:
    # Create tableau from stabilizers
    # Note: stim.Tableau.from_stabilizers expects a list of stim.PauliString
    pauli_stabilizers = [stim.PauliString(s) for s in stabilizers_str]
    tableau = stim.Tableau.from_stabilizers(pauli_stabilizers, allow_underconstrained=True)
    
    print(f"Tableau created. Qubits: {len(tableau)}")
    
    # 1. Elimination Method
    c_elim = tableau.to_circuit(method="elimination")
    cx_elim = sum(1 for op in c_elim if op.name == "CX")
    print(f"Elimination Method: CX={cx_elim}, Total={len(c_elim)}")
    
    # 2. Graph State Method
    c_graph = tableau.to_circuit(method="graph_state")
    cx_graph = sum(1 for op in c_graph if op.name == "CX")
    cz_graph = sum(1 for op in c_graph if op.name == "CZ")
    print(f"Graph State Method: CX={cx_graph}, CZ={cz_graph}, Total={len(c_graph)}")
    
    # Save candidates to files
    with open(r"C:\Users\anpaz\Repos\quantum-ai\rq3\candidate_elim.stim", "w") as f:
        f.write(str(c_elim))
    
    with open(r"C:\Users\anpaz\Repos\quantum-ai\rq3\candidate_graph.stim", "w") as f:
        f.write(str(c_graph))

except Exception as e:
    print(f"Error: {e}")
